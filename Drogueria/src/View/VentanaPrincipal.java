    private void SubItemMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubItemMenuActionPerformed
        if (ListaFactura.TablaFacturas.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "No hay Datos en la Tabla de Factura");
        } else {
            JFileChooser chooser = new JFileChooser();
            FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivo de Excel", "xls");
            chooser.setFileFilter(filter);
            chooser.setDialogTitle("Guardar Archivo");
            chooser.setMultiSelectionEnabled(false);
            chooser.setAcceptAllFileFilterUsed(false);
            if (chooser.showSaveDialog(null) == JFileChooser.APPROVE_OPTION) {
                List<JTable> tb = new ArrayList<>();
                List<String> nom = new ArrayList<>();
                tb.add(ListaFactura.TablaFacturas);
                nom.add("Facturas");
                String file = chooser.getSelectedFile().toString().concat(".xls");
                try {
                    Exportar e = new Exportar(new File(file), tb, nom);
                    if (e.export()) {
                        JOptionPane.showMessageDialog(null, "Facturas Exportadas");
                        Sistema sistema = new Sistema();
                        sistema.InsertarHistorial("Ha Exportado Las Facturas");
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Hubo un Error :" + e);
                }
            }
        }
    }//GEN-LAST:event_SubItemMenuActionPerformed

