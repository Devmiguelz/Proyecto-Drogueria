    private void Empresa() {
        Empresa empresa = new Empresa();
        if (!empresa.RegistroEmpresa()) {
            JOptionPane.showMessageDialog(rootPane, "Datos de la empresa no registrada");
            new DatosEmpresa().setVisible(true);
        }
    }

    public void Confirmar() {
        int resp = JOptionPane.showConfirmDialog(null, "Â¿Desea cerrar el Programa?", "Confirmacion", JOptionPane.YES_NO_OPTION);
        if (resp == 0) {
            Sistema sistema = new Sistema();
            sistema.InsertarHistorial("Ha Cerrado Sesion");
            System.exit(0);
        }
    }

