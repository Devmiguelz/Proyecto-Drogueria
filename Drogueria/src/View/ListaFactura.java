    private void TxtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtBuscarKeyReleased
        String texto = TxtBuscar.getText();
        if (!texto.equals("")) {
            int codigo = 0;
            try {
                codigo = Integer.parseInt(texto);
            } catch (Exception e) {
            }
            factura.Buscar(codigo);
        }else{
            factura.TablaFactura();
        }
    }//GEN-LAST:event_TxtBuscarKeyReleased

    private void TablaFacturasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaFacturasMousePressed
        int fila = TablaFacturas.getSelectedRow();
        if (fila >= 0) {
            int codigo = Integer.parseInt(TablaFacturas.getValueAt(fila,0).toString());
            factura.ObtenerDetalles(codigo);
        }
    }//GEN-LAST:event_TablaFacturasMousePressed

    private void formMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseClicked
        TablaFacturas.clearSelection();
        TablaDetalle.clearSelection();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Precio");
        modelo.addColumn("SubTotal");
        TablaDetalle.setModel(modelo);
    }//GEN-LAST:event_formMouseClicked

