    private void SetTotal() {
         DecimalFormat formato = new DecimalFormat("###,###.##");
        float total = 0;
        for (int i = 0; i < TablaFacturas.getRowCount(); i++) {
            total += Float.parseFloat(TablaFacturas.getValueAt(i, 5).toString());
        }
        LTotal.setText(String.valueOf(formato.format(total)));
    }

